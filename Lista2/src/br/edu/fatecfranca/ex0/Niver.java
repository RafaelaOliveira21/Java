package br.edu.fatecfranca.ex0;

public class Niver {

	private int dia;
	private String mes;

	public Niver() {

	}

	public Niver(int dia, String mes) {
		this.dia = dia;
		this.mes = mes;
	}

	public int getDia() {
		return this.dia;
	}
	
	public void setDia(int dia) {
		this.dia = dia;
	}

	public String getMes() {
		return this.mes;
	}
	
	public void setMes(String mes) {
		this.mes = mes;
	}
}
